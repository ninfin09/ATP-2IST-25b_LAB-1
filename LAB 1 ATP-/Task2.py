for line in open(0):              # Читаємо кожен рядок з вхідних даних
    n = line.strip()              # Видаляємо пробіли та символи нового рядка
    nn = n + n                    # Складаємо два рази n для отримання "nn"
    nnn = n + n + n               # Складаємо три рази n для отримання "nnn"
    result = int(n) + int(nn) + int(nnn)  # Обчислюємо суму n + nn + nnn
    print(result)       # Виводимо результат