x1 = float(input())                    # Зчитуємо x1
y1 = float(input())                    # Зчитуємо y1
x2 = float(input())                    # Зчитуємо x2
y2 = float(input())                    # Зчитуємо y2

mx = round((x1 + x2) / 2, 2)           # Обчислюємо та округлюємо x середини
my = round((y1 + y2) / 2, 2)           # Обчислюємо та округлюємо y середини

print("The midpoint's x value is:", mx)  # Виводимо x середини
print("The midpoint's y value is:", my)  # Виводимо y середини